package com.ojiofong.arounda;

import java.util.ArrayList;
import java.util.HashMap;

public class Wrapper {
	
	ArrayList<HashMap<String, String>> wrapperList;
	ArrayList<String> simpleList;

	public ArrayList<HashMap<String, String>> getWrapperList() {
		return wrapperList;
	}

	public void setWrapperList(ArrayList<HashMap<String, String>> wrapperList) {
		this.wrapperList = wrapperList;
	}

	public ArrayList<String> getSimpleList() {
		return simpleList;
	}

	public void setSimpleList(ArrayList<String> simpleList) {
		this.simpleList = simpleList;
	}
	
	

}
